class Freeze extends Water
{
    protected $defaultCooldownMin = 4;
    protected $defaultCooldownMax = 5;

    protected function spellEffect($data)
    {
        $target = $this->targets[0];
        $target->freeze(3, Game::ANIMATION_STAGE_MAGE_ACTION_EFFECT);
        return true;
    }
}