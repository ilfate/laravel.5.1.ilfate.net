trait FireDamageSpell
{
    protected function spellEffect($data)
    {

        foreach($this->targets as $target) {
            /**
             * @var Unit $target
             */
            $target->damage($this->getDamage(), $this->getNormalCastStage());
        }
        $this->destroyTreesAtCells($this->affectedCells);
        return true;
    }
}